int	ft_iterative_factorial(int nb)
{
	int	rtn_num;

	if (nb < 0)
		return (0);
	rtn_num = 1;
	while (nb)
	{
		rtn_num *= nb;
		nb--;
	}
	return (rtn_num);
}
