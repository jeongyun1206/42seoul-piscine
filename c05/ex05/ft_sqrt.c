int	ft_sqrt(int nb)
{
	int	sqrt;

	if (nb == 1)
		return (1);
	sqrt = 1;
	while (sqrt <= nb / sqrt)
	{
		if (nb == sqrt * sqrt)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
