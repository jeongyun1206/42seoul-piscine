int	ft_is_prime(int nb)
{
	int	i;
	int	prime;

	if (nb == 0)
		return (0);
	if (nb == 1)
		return (0);
	i = 2;
	while (i < nb)
	{
		prime = nb / i;
		if (prime * i == nb)
			return (0);
		i++;
	}
	return (1);
}
