char	*ft_strlowcase(char	*str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if ((str[idx] >= 'A' ) && (str[idx] <= 'Z'))
			str[idx] -= 'A' - 'a';
		idx++;
	}
	return (str);
}
